export function SocialIcon({
  url,
  className,
}: {
  url: string
  className?: string
}) {
  const platform = detectPlatform(url)
  const Icon = platform ? ICON_MAP[platform] : null

  if (Icon) {
    return <Icon className={cn("size-4", className)} />
  }

  return <LinkIcon className={cn("size-4", className)} />
}
